package com.yuyuka.billiards.pojo;

public class ModularPojo {
    private int icon;
    private String title;

    public ModularPojo(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }

}
